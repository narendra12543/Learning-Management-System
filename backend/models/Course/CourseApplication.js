import mongoose from 'mongoose';

const courseApplicationSchema = new mongoose.Schema({
  user: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'User',
    required: true
  },
  course: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'Course',
    required: true
  },
  status: {
    type: String,
    enum: ['pending', 'approved', 'rejected'],
    default: 'pending'
  },
  applicationDate: {
    type: Date,
    default: Date.now
  },
  reviewedAt: {
    type: Date
  },
  reviewedBy: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'User'
  },
  notes: {
    type: String
  }
}, {
  timestamps: true
});

// Index for efficient queries
courseApplicationSchema.index({ user: 1, course: 1 }, { unique: true });
courseApplicationSchema.index({ status: 1 });
courseApplicationSchema.index({ createdAt: -1 });

export default mongoose.model('CourseApplication', courseApplicationSchema);
